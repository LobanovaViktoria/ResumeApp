import UIKit

extension UIColor {
    // Text Colors
    static let textColorBlack = UIColor(named: "textColorBlack") ?? UIColor.black
    static let textColorGray = UIColor(named: "textColorGray") ?? UIColor.gray
    
    // Background Colors
    static let backgroundGray = UIColor(named: "backgroundGray") ?? UIColor.gray
    static let backgroundWhite = UIColor(named: "backgroundWhite") ?? UIColor.white
}
